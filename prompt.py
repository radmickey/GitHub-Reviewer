def build_prompt(diff: str, skipped: list, readme: str, extra_instruction: str) -> str:
    readme_block = ""
    if readme:
        readme_block = f"""
## README задания

{readme[:3000]}
"""

    extra_block = ""
    if extra_instruction:
        extra_block = f"\n## Дополнительная инструкция от преподавателя\n{extra_instruction}\n"

    skipped_block = ""
    if skipped:
        skipped_block = f"\n## Пропущенные файлы\nСледующие файлы были исключены из ревью: {', '.join(skipped)}\n"

    return f"""Ты — опытный преподаватель C++ в университете. Твоя задача — проверить лабораторную работу студента и подготовить подробный отчёт для преподавателя.

{extra_block}{skipped_block}
## Что нужно сделать

Проанализируй diff лабораторной работы и напиши отчёт по следующей структуре:

### ✅ Что сделано хорошо
Перечисли конкретные вещи, которые студент сделал правильно. Не будь скупым — если что-то хорошо, скажи об этом явно. Это важно для мотивации.

### ❌ Проблемы и ошибки
Перечисли конкретные проблемы с указанием на код. Для каждой проблемы:
- Что именно не так
- Почему это проблема (последствия)
- Как исправить

Раздели на критические (влияют на корректность/безопасность) и некритические (стиль, читаемость).

### 👀 На что обратить внимание преподавателю
Вещи, которые не являются явными ошибками, но стоит обсудить со студентом на защите: сомнительные решения, неоптимальные подходы, места где студент явно не разобрался но случайно сделал правильно.

### 🎓 Вопросы для защиты
Если в README есть раздел с теоретическим минимумом (список тем, вопросов или понятий — он может называться по-разному), найди его и придумай по 1-2 конкретных вопроса на каждую тему. Вопросы должны проверять реальное понимание, а не зазубренные определения. Если такого раздела нет — пропусти этот блок.
{readme_block}
### 📊 Общая оценка
Краткое резюме в 2-3 предложения: общий уровень работы, готова ли к защите, что главное доработать.

---

Пиши по-русски. Будь конкретным — ссылайся на реальный код из diff. Не придумывай проблем которых нет.

## Diff лабораторной работы

{diff}"""
